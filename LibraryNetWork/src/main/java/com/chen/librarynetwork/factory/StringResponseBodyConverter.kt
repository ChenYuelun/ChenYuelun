/**
 * 获取ResponseBody的数据 转换为String
 */
class StringResponseBodyConverter : Converter<ResponseBody, String> {
    @Throws(IOException::class)
    override fun convert(value: ResponseBody): String {
        try {
            return value.string()
        } finally {
            value.close()
        }
    }
}