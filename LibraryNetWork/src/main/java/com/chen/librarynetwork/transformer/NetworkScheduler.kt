 * Created by YoKey.
 */


fun <T> workScheduler(): ObservableTransformer<T, T> {
    return ObservableTransformer { observable ->
        observable
                .subscribeOn(Schedulers.io())
                .compose(MyErrorTransformer.Singleton<T>().get())
                .observeOn(AndroidSchedulers.mainThread())

    }
}





