abstract class BaseSubscriber<T> : Subscriber<T> {


    override fun onError(e: Throwable) {
        if (e is ApiException) {
            onError(e)
        } else {
            onError(ApiException(e, 1000))
        }
    }


    /**
     * 错误回调
     */
    protected abstract fun onError(ex: ApiException)


}